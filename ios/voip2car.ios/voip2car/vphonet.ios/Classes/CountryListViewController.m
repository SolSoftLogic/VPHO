	NSMutableArray	*countries = [[VPCountries singleton] list];
    // Return the number of rows in the section.
    return [countries count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *cellIdentifier = @"CellCountryListViewController";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:cellIdentifier] autorelease];
    }
    
	NSMutableArray	*countries = [[VPCountries singleton] list];
	Country* country = (Country*)[countries objectAtIndex:indexPath.row];

	if (country) {
		cell.imageView.image = [UIImage imageNamed:[NSString stringWithFormat:@"%@.png", [country.iso2 lowercaseString]]];
		
		//[UIImage imageWithContentsOfFile:[[NSBundle mainBundle] pathForResource:[country.iso2 lowercaseString] ofType:@"png"]];;//[UIImage imageNamed:[NSString stringWithFormat:@"%@.png", country.iso2]];
		cell.textLabel.text = NSLocalizedString(country.name, @"");
		if ([country.code length] != 0 ) {
			cell.detailTextLabel.text =  [NSString stringWithFormat:@"+%@", country.code];
		}
	}

    
    return cell;
}

